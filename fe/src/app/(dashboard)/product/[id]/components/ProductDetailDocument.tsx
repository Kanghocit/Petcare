const COLLAPSED_HEIGHT = 350;

const ProductDetailDocument = () => {
  const [expanded, setExpanded] = useState(false);
  const [maxHeight, setMaxHeight] = useState(COLLAPSED_HEIGHT);
  const contentRef = useRef<HTMLDivElement>(null);

  useLayoutEffect(() => {
    if (expanded && contentRef.current) {
      setMaxHeight(contentRef.current.scrollHeight);
    } else {
      setMaxHeight(COLLAPSED_HEIGHT);
    }
  }, [expanded]);

  return (
    <div className="bg-white rounded-lg p-6 mt-6 shadow">
      <h2 className="text-xl font-bold mb-4 border-b pb-2">Mô tả sản phẩm</h2>
      <div
        ref={contentRef}
        className="relative transition-all duration-500 ease-in-out"
        style={{
          maxHeight: expanded ? maxHeight : COLLAPSED_HEIGHT,
          opacity: expanded ? 1 : 0.95,
          overflow: "hidden",
          WebkitMaskImage: !expanded
            ? "linear-gradient(180deg, #000 60%, transparent)"
            : undefined,
        }}
      >
        <pre className="whitespace-pre-wrap text-gray-700 text-base font-sans bg-transparent">
          {description}
        </pre>
      </div>
      <div className="flex justify-center mt-4">
        <button
          className="px-8 py-2 rounded-md bg-blue-700 hover:bg-blue-800 text-white font-bold text-lg shadow transition-all duration-200 transform hover:scale-105 hover:shadow-xl hover:bg-gradient-to-r hover:from-blue-700 hover:to-blue-500"
          onClick={() => setExpanded((e) => !e)}
        >
          {expanded ? "THU GỌN ▲" : "XEM THÊM ▼"}
        </button>
      </div>
    </div>
  );
};

export default ProductDetailDocument;
